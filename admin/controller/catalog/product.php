	public function downloadImage() {
		$this->load->language('catalog/product');
		$json = array();

		// Verificar token de segurança
		if (!isset($this->session->data['user_token']) || !isset($this->request->post['user_token']) || ($this->session->data['user_token'] != $this->request->post['user_token'])) {
			$json['error'] = 'Token de segurança inválido!';
		}

		// Verificar permissões
		if (!$this->user->hasPermission('modify', 'catalog/product')) {
			$json['error'] = 'Você não tem permissão para modificar produtos!';
		}

		// Validar dados obrigatórios
		if (!isset($this->request->post['product_id']) || !isset($this->request->post['image_url']) || !isset($this->request->post['product_name'])) {
			$json['error'] = 'Dados obrigatórios não fornecidos!';
		}

		if (!$json) {
			$this->load->model('catalog/product');
			$this->load->model('tool/image');

			$product_id = (int)$this->request->post['product_id'];
			$image_url = trim($this->request->post['image_url']);
			$product_name = trim($this->request->post['product_name']);

			// Validar se produto existe
			$product_info = $this->model_catalog_product->getProduct($product_id);
			if (!$product_info) {
				$json['error'] = 'Produto não encontrado!';
			} else {
				try {
					// Verificar se é base64 ou URL
					$is_base64 = $this->isBase64Image($image_url);
					
					if ($is_base64) {
						// Processar imagem base64
						$image_data = $this->processBase64Image($image_url);
						if (!$image_data) {
							$json['error'] = 'Dados base64 inválidos ou formato não suportado!';
							throw new Exception('Base64 inválido');
						}
						$file_extension = $image_data['extension'];
						$image_binary = $image_data['data'];
					} else {
						// Processar URL normal
						if (!filter_var($image_url, FILTER_VALIDATE_URL)) {
							$json['error'] = 'URL da imagem inválida!';
							throw new Exception('URL inválida');
						}

						// Baixar a imagem
						$image_binary = $this->downloadImageFromUrl($image_url);
						if (!$image_binary) {
							$json['error'] = 'Não foi possível baixar a imagem da URL fornecida!';
							throw new Exception('Falha no download');
						}
						
						$file_extension = $this->getImageExtension($image_url);
					}

					// Validar se é uma imagem válida
					if (!$this->validateImageData($image_binary)) {
						$json['error'] = 'O arquivo não é uma imagem válida!';
						throw new Exception('Imagem inválida');
					}

					// Processar e otimizar a imagem
					$processed_image = $this->processAndOptimizeImage($image_binary, $file_extension);
					if (!$processed_image) {
						$json['error'] = 'Erro ao processar a imagem!';
						throw new Exception('Erro no processamento');
					}

					$image_binary = $processed_image['data'];
					$file_extension = $processed_image['extension'];

					// Criar diretório se não existir
					$upload_dir = DIR_IMAGE . 'catalog/images-downloads/';
					if (!is_dir($upload_dir)) {
						mkdir($upload_dir, 0755, true);
					}

					// Gerar nome do arquivo baseado no nome do produto
					$safe_name = $this->sanitizeFileName($product_name);
					$filename = $safe_name . '_' . $product_id . '.' . $file_extension;
					$filepath = $upload_dir . $filename;

					// Salvar arquivo
					if (!file_put_contents($filepath, $image_binary)) {
						$json['error'] = 'Erro ao salvar a imagem no servidor!';
						throw new Exception('Erro ao salvar');
					}

					// Atualizar banco de dados
					$db_image_path = 'catalog/images-downloads/' . $filename;
					$this->model_catalog_product->updateProductImage($product_id, $db_image_path);

					// Gerar URL da imagem para exibição
					$image_url_display = $this->model_tool_image->resize($db_image_path, 40, 40);

					$json['success'] = true;
					$json['message'] = 'Imagem atualizada com sucesso!';
					$json['image_url'] = $image_url_display;
					$json['image_path'] = $db_image_path;

					// Adicionar informações de processamento se a imagem foi otimizada
					if (isset($processed_image['original_size']) && $processed_image['original_size'] != $processed_image['new_size']) {
						$original_size_mb = round($processed_image['original_size'] / (1024 * 1024), 2);
						$new_size_mb = round($processed_image['new_size'] / (1024 * 1024), 2);
						$savings_percent = round((($processed_image['original_size'] - $processed_image['new_size']) / $processed_image['original_size']) * 100, 1);
						
						$optimization_info = array();
						
						if ($processed_image['original_dimensions'] != $processed_image['new_dimensions']) {
							$optimization_info[] = "Redimensionada de {$processed_image['original_dimensions']} para {$processed_image['new_dimensions']}";
						}
						
						if ($original_size_mb != $new_size_mb) {
							$optimization_info[] = "Comprimida de {$original_size_mb}MB para {$new_size_mb}MB (economia de {$savings_percent}%)";
						}
						
						if (!empty($optimization_info)) {
							$json['message'] .= '<br><small class="text-muted">Otimizações: ' . implode(' | ', $optimization_info) . '</small>';
						}
					}

				} catch (Exception $e) {
					if (!isset($json['error'])) {
						$json['error'] = 'Erro interno: ' . $e->getMessage();
					}
				}
			}
		}

		$this->response->addHeader('Content-Type: application/json');
		$this->response->setOutput(json_encode($json));
	}

	private function sanitizeFileName($name) {
		// Converter para minúsculas
		$name = strtolower($name);
		
		// Remover acentos
		$name = $this->removeAccents($name);
		
		// Manter apenas letras, números e espaços
		$name = preg_replace('/[^a-z0-9\s]/', '', $name);
		
		// Substituir espaços por underscores
		$name = preg_replace('/\s+/', '_', $name);
		
		// Remover underscores múltiplos
		$name = preg_replace('/_+/', '_', $name);
		
		// Remover underscores do início e fim
		$name = trim($name, '_');
		
		// Limitar tamanho
		if (strlen($name) > 50) {
			$name = substr($name, 0, 50);
		}
		
		return $name ?: 'produto';
	}

	private function removeAccents($string) {
		$accents = array(
			'à', 'á', 'â', 'ã', 'ä', 'å', 'æ', 'ç', 'è', 'é', 'ê', 'ë', 'ì', 'í', 'î', 'ï',
			'ð', 'ñ', 'ò', 'ó', 'ô', 'õ', 'ö', 'ø', 'ù', 'ú', 'û', 'ü', 'ý', 'þ', 'ÿ'
		);
		
		$no_accents = array(
			'a', 'a', 'a', 'a', 'a', 'a', 'ae', 'c', 'e', 'e', 'e', 'e', 'i', 'i', 'i', 'i',
			'd', 'n', 'o', 'o', 'o', 'o', 'o', 'o', 'u', 'u', 'u', 'u', 'y', 'th', 'y'
		);
		
		return str_replace($accents, $no_accents, $string);
	}

	private function getImageExtension($url) {
		// Tentar pegar extensão da URL
		$path_info = pathinfo(parse_url($url, PHP_URL_PATH));
		$extension = isset($path_info['extension']) ? strtolower($path_info['extension']) : '';
		
		// Extensões permitidas
		$allowed = array('jpg', 'jpeg', 'png', 'gif', 'webp');
		
		if (in_array($extension, $allowed)) {
			return $extension;
		}
		
		// Se não conseguir identificar, usar jpg como padrão
		return 'jpg';
	}

	private function downloadImageFromUrl($url) {
		// Configurar contexto para requisição HTTP
		$context = stream_context_create(array(
			'http' => array(
				'timeout' => 30,
				'user_agent' => 'Mozilla/5.0 (Windows NT 10.0; Win64; x64) AppleWebKit/537.36 (KHTML, like Gecko) Chrome/91.0.4472.124 Safari/537.36'
			)
		));

		// Tentar baixar usando file_get_contents
		$image_data = @file_get_contents($url, false, $context);
		
		if ($image_data === false && function_exists('curl_init')) {
			// Fallback para cURL se file_get_contents falhar
			$ch = curl_init();
			curl_setopt($ch, CURLOPT_URL, $url);
			curl_setopt($ch, CURLOPT_RETURNTRANSFER, true);
			curl_setopt($ch, CURLOPT_TIMEOUT, 30);
			curl_setopt($ch, CURLOPT_USERAGENT, 'Mozilla/5.0 (Windows NT 10.0; Win64; x64) AppleWebKit/537.36 (KHTML, like Gecko) Chrome/91.0.4472.124 Safari/537.36');
			curl_setopt($ch, CURLOPT_SSL_VERIFYPEER, false);
			
			$image_data = curl_exec($ch);
			$http_code = curl_getinfo($ch, CURLINFO_HTTP_CODE);
			curl_close($ch);
			
			if ($http_code !== 200 || $image_data === false) {
				return false;
			}
		}

		return $image_data;
	}

	private function isBase64Image($string) {
		// Verificar se a string contém data:image
		if (preg_match('/^data:image\/(png|jpg|jpeg|gif|webp);base64,/', $string)) {
			return true;
		}
		
		// Verificar se é base64 puro (sem prefixo data:)
		if (base64_encode(base64_decode($string, true)) === $string) {
			// Tentar decodificar e verificar se é imagem
			$decoded = base64_decode($string, true);
			return $this->validateImageData($decoded);
		}
		
		return false;
	}

	private function processBase64Image($base64_string) {
		$result = array();
		
		// Verificar se tem prefixo data:image
		if (preg_match('/^data:image\/(\w+);base64,(.+)$/', $base64_string, $matches)) {
			$extension = strtolower($matches[1]);
			$data = base64_decode($matches[2], true);
			
			// Normalizar extensões
			if ($extension === 'jpeg') {
				$extension = 'jpg';
			}
			
			// Verificar se a extensão é suportada
			$allowed = array('jpg', 'jpeg', 'png', 'gif', 'webp');
			if (!in_array($extension, $allowed)) {
				return false;
			}
			
			$result['extension'] = $extension;
			$result['data'] = $data;
			
		} else {
			// Base64 puro, tentar decodificar
			$data = base64_decode($base64_string, true);
			if ($data === false) {
				return false;
			}
			
			// Detectar tipo de imagem pelos bytes
			$extension = $this->detectImageTypeFromData($data);
			if (!$extension) {
				return false;
			}
			
			$result['extension'] = $extension;
			$result['data'] = $data;
		}
		
		return $result;
	}

	private function detectImageTypeFromData($data) {
		// Verificar assinatura de imagens conhecidas
		$signatures = array(
			"\xFF\xD8\xFF" => 'jpg',
			"\x89PNG\r\n\x1a\n" => 'png',
			"GIF87a" => 'gif',
			"GIF89a" => 'gif'
		);

		foreach ($signatures as $signature => $type) {
			if (substr($data, 0, strlen($signature)) === $signature) {
				return $type;
			}
		}

		// Verificar WebP
		if (substr($data, 0, 4) === 'RIFF' && substr($data, 8, 4) === 'WEBP') {
			return 'webp';
		}

		return false;
	}

	private function validateImageData($data) {
		if (empty($data)) {
			return false;
		}

		// Verificar assinatura de imagens conhecidas
		$signatures = array(
			"\xFF\xD8\xFF" => 'jpg',
			"\x89PNG\r\n\x1a\n" => 'png',
			"GIF87a" => 'gif',
			"GIF89a" => 'gif',
			"RIFF" => 'webp'
		);

		foreach ($signatures as $signature => $type) {
			if (strpos($data, $signature) === 0) {
				return true;
			}
		}

		return false;
	}	

	private function processAndOptimizeImage($image_data, $extension) {
		$max_dimension = 1000; // Dimensão máxima permitida
		$max_file_size = 2 * 1024 * 1024; // 2MB em bytes
		$current_size = strlen($image_data);
		
		try {
			// Criar resource de imagem baseado na extensão
			$image_resource = $this->createImageResource($image_data, $extension);
			if (!$image_resource) {
				return false;
			}

			// Obter dimensões originais
			$original_width = imagesx($image_resource);
			$original_height = imagesy($image_resource);
			
			// Verificar se precisa redimensionar
			$needs_resize = ($original_width > $max_dimension || $original_height > $max_dimension);
			$needs_compression = ($current_size > $max_file_size);
			
			if (!$needs_resize && !$needs_compression) {
				// Não precisa processar
				imagedestroy($image_resource);
				return array(
					'data' => $image_data,
					'extension' => $extension
				);
			}

			// Calcular novas dimensões mantendo proporção
			if ($needs_resize) {
				if ($original_width > $original_height) {
					$new_width = min($original_width, $max_dimension);
					$new_height = intval(($original_height * $new_width) / $original_width);
				} else {
					$new_height = min($original_height, $max_dimension);
					$new_width = intval(($original_width * $new_height) / $original_height);
				}
			} else {
				$new_width = $original_width;
				$new_height = $original_height;
			}

			// Criar nova imagem redimensionada
			$new_image = imagecreatetruecolor($new_width, $new_height);
			
			// Preservar transparência para PNG e GIF
			if ($extension == 'png' || $extension == 'gif') {
				imagealphablending($new_image, false);
				imagesavealpha($new_image, true);
				$transparent = imagecolorallocatealpha($new_image, 255, 255, 255, 127);
				imagefilledrectangle($new_image, 0, 0, $new_width, $new_height, $transparent);
			}

			// Redimensionar
			imagecopyresampled($new_image, $image_resource, 0, 0, 0, 0, 
							   $new_width, $new_height, $original_width, $original_height);

			// Determinar qualidade de compressão
			$quality = 85; // Qualidade padrão
			if ($needs_compression) {
				// Calcular qualidade baseada no tamanho
				$size_ratio = $max_file_size / $current_size;
				$quality = max(60, min(85, intval(85 * $size_ratio)));
			}

			// Converter para dados binários
			ob_start();
			$success = false;
			
			switch ($extension) {
				case 'jpg':
				case 'jpeg':
					$success = imagejpeg($new_image, null, $quality);
					break;
				case 'png':
					// Para PNG, converter qualidade (0-9 onde 9 é máxima compressão)
					$png_quality = 9 - intval(($quality / 100) * 9);
					$success = imagepng($new_image, null, $png_quality);
					break;
				case 'gif':
					$success = imagegif($new_image);
					break;
				case 'webp':
					$success = imagewebp($new_image, null, $quality);
					break;
			}

			$processed_data = ob_get_contents();
			ob_end_clean();

			// Limpar recursos
			imagedestroy($image_resource);
			imagedestroy($new_image);

			if (!$success || empty($processed_data)) {
				return false;
			}

			return array(
				'data' => $processed_data,
				'extension' => $extension,
				'original_size' => $current_size,
				'new_size' => strlen($processed_data),
				'original_dimensions' => $original_width . 'x' . $original_height,
				'new_dimensions' => $new_width . 'x' . $new_height
			);

		} catch (Exception $e) {
			return false;
		}
	}

	private function createImageResource($image_data, $extension) {
		try {
			switch ($extension) {
				case 'jpg':
				case 'jpeg':
					return imagecreatefromstring($image_data);
				case 'png':
					return imagecreatefromstring($image_data);
				case 'gif':
					return imagecreatefromstring($image_data);
				case 'webp':
					return imagecreatefromstring($image_data);
				default:
					return false;
			}
		} catch (Exception $e) {
			return false;
		}
	}
